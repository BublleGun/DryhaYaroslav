public class Dog {
    private String name;
    private int age;

    public void SetName(String p_name)
    {
        this.name = p_name;
    }

    public String GetName()
    {
        return this.name;
    }
    public void SetAge(int p_age)
    {
        this.age = p_age;
    }

    public int GetAge()
    {
        return this.age;
    }


}
